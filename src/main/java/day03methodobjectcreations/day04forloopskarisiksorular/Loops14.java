package day03methodobjectcreations.day04forloopskarisiksorular;

public class Loops14 {
    public static void main(String[] args) {

      // çarpım tablosunu ekrana yazdırınız.


        for (int i = 1; i < 11; i++) {

            for (int j = 1; j < 11; j++) {


                System.out.println(i + "x" + j + "=" + i * j);


            }

            System.out.println();
        }
    }
}